unsigned char i;
unsigned char bitmux[16]={
  0b00000000,//0
  0b00000001,//1
  0b00000010,//2
  0b00000011,//3
  0b00000100,//4
  0b00000101,//5
  0b00000110,//6
  0b00000111,//7
  0b00001000,//8
  0b00001001,//9
  0b00001010,//a
  0b00001011,//b
  0b00001100,//c
  0b00001101,//d
  0b00001110,//e
  0b00001111 //f
};

void setup() {
  DDRB = 0x0F;

  DDRD |= (1<<7)|(1<<3); //0x88 0b10001000
  PORTD |= (1<<3);
  PORTD &= ~(1<<7);
}

void loop() {
  for(i=0;i<4;i++){
    PORTB = bitmux[i];
    delay(100);
  }
}
